static const char hex_chars[] = "0123456789abcdef";

/* Xorshift128+ PRNG state */
static uint64_t xorshift_state[2];
static int xorshift_initialized = 0;

/* Xorshift128+ algorithm (matches Bitmain implementation) */
static uint64_t xorshift128plus(void)
{
    uint64_t s1 = xorshift_state[0];
    uint64_t s0 = xorshift_state[1];
    uint64_t result = s0 + s1;

    xorshift_state[0] = s0;
    s1 ^= s1 << 23;
    s1 ^= s1 >> 18;
    s1 ^= s0;
    s1 ^= s0 >> 5;
    xorshift_state[1] = s1;

    return result;
}

/* Generate UUID v4 string using Xorshift128+ PRNG */
static int generate_uuid(char *uuid_buf)
{
    int fd;
    ssize_t n;
    const char *template = "xxxxxxxxxxxx4xxxyxxxxxxxxxxxxxxx";
    uint64_t random_bytes[2];
    int i, byte_index = 0;

    /* Initialize PRNG if needed */
    if (!xorshift_initialized) {
        fd = open("/dev/urandom", O_RDONLY);
        if (fd < 0)
            return -1;

        n = read(fd, xorshift_state, sizeof(xorshift_state));
        close(fd);

        if (n != sizeof(xorshift_state))
            return -1;

        /* Ensure non-zero state */
        if (xorshift_state[0] == 0 && xorshift_state[1] == 0)
            return -1;

        xorshift_initialized = 1;
    }

    /* Generate two 64-bit random values */
    random_bytes[0] = xorshift128plus();
    random_bytes[1] = xorshift128plus();

    /* Fill UUID template */
    for (i = 0; template[i]; i++) {
        uint8_t byte = ((uint8_t *)random_bytes)[byte_index / 2];
        uint8_t nibble = (byte_index & 1) ? (byte >> 4) : (byte & 0x0F);

        if (template[i] == 'x') {
            uuid_buf[i] = hex_chars[nibble];
            byte_index++;
        } else if (template[i] == 'y') {
            /* UUID v4: bits 6-7 must be 10b */
            uuid_buf[i] = hex_chars[(nibble & 0x03) + 8];
            byte_index++;
        } else {
            /* Keep template character (e.g., '4' for version) */
            uuid_buf[i] = template[i];
        }
    }
    uuid_buf[i] = '\0';

    return 0;
}

/* Convert UUID/CPUID string to MAC address using CRC16 + CRC32 */
static void uuid_to_mac(const char *uuid, char *mac_str)
{
    const uint8_t *data = (const uint8_t *)uuid;
    size_t len = strlen(uuid);
    uint16_t crc16 = 0;
    uint32_t crc32 = 0xFFFFFFFF;
    size_t i;

    /* Calculate CRC16 */
    for (i = 0; i < len; i++) {
        crc16 = (crc16 << 8) ^ crc16_table[data[i] ^ (crc16 >> 8)];
    }

    /* Calculate CRC32 */
    for (i = 0; i < len; i++) {
        crc32 = (crc32 << 8) ^ crc32_table[data[i] ^ (crc32 >> 24)];
    }

    /* Extract 6 bytes for MAC address */
    uint8_t mac[6];
    mac[0] = (crc16 >> 8) & 0xFE;  /* Ensure unicast (bit 0 clear) */
    mac[1] = crc16 & 0xFF;
    mac[2] = crc32 & 0xFF;
    mac[3] = (crc32 >> 8) & 0xFF;
    mac[4] = (crc32 >> 16) & 0xFF;
    mac[5] = (crc32 >> 24) & 0xFF;

    /* Format as MAC string */
    sprintf(mac_str, "%02x:%02x:%02x:%02x:%02x:%02x",
            mac[0], mac[1], mac[2], mac[3], mac[4], mac[5]);
}

int main(void)
{
    char uuid[64] = {0};
    char mac[20];
    char *cpuid;

    /* Check for CPUID environment variable */
    cpuid = getenv("CPUID");
    if (cpuid && strlen(cpuid) > 0) {
        strncpy(uuid, cpuid, sizeof(uuid) - 1);
    } else {
        /* Generate UUID v4 */
        if (generate_uuid(uuid) != 0) {
            /* Fallback: return default MAC */
            printf("ff:ff:ff:ff:ff:ff\n");
            return 1;
        }
    }

    /* Convert to MAC address */
    uuid_to_mac(uuid, mac);

    /* Print MAC address */
    printf("%s\n", mac);

    return 0;
}
